package mathematics;

import java.util.*;
    public class assignmentOperator {
    public static void main(String[] args) {


        int a = 20;
        int b=50;

        // +=
        a+=b;
        System.out.println(a);

        //-=
        a-=b;
        System.out.println(a);

        // *=
        a*=b;
        System.out.println(a);

        // /=
        a/=b;
        System.out.println(a);

    }

}
